public class Diem {
    private int x;
    private int y;

    //Constructor
    public Diem() {}
    public Diem(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //Getter-Setter
    public int getX() {return x;}
    public int getY() {return y;}
    public void setX(int x) {this.x = x;}
    public void setY(int y) {this.y = y;}

}
